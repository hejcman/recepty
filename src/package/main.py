def add_numbers(num1: float, num2: float) -> float:
    """Add two numbers.

    Args:
        num1 (float): The first number.
        num2 (float): The number to add to the first number.

    Returns:
        The sum of the two numbers.
    """
    return num1 + num2


def sub_numbers(num1: float, num2: float) -> float:
    """Subtract one number from another.

    Args:
        num1 (float): The first number.
        num2 (float): The number to subtract from the first number.

    Returns:
        The first number minus the second number.
    """
    return num1 - num2
